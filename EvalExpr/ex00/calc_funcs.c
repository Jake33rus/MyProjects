int		ft_sum(int a, int b)
{
	return (b + a);
}

int		ft_sub(int a, int b)
{
	return (b - a);
}

int		ft_mul(int a, int b)
{
	return (b * a);
}

int		ft_div(int a, int b)
{
	return (b / a);
}

int		ft_mod(int a, int b)
{
	return (b % a);
}
